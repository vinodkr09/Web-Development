console.log(2 > 1);  // output will be True
console.log(2 >= 1); // output will be True
console.log(2 < 1);  // output will be False
console.log(2 == 1);  // output will be false
console.log(2 != 1);  // output will be True

// Generally we avoid below data type comparsion bcz of its inconsistent result

// comparison of two different data Type i.e "String" and "Number" 
console.log("2" > 1);   // here javascript automatically converts string into number. Hence otput will be True
console.log("02" > 1);  // here javascript automatically converts string into number. Hence otput will be True

